fun main() {
    //Integer Type
    val age: Byte = 20
    val weight: Short = 100
    val distance: Int = 86800
    val balance: Long = 300_000_000L

    println(age)
    println(weight)
    println(distance)
    println(balance)

    //Unsigned Integer
    val uAge: UByte = 20u
    val uWeight: UShort = 120u
    val udistance: UInt = 868000u
    val ubalance: ULong = 300_000_000u

    //Format kode pakai ctrl+alt+L agar tulisan rapi
}